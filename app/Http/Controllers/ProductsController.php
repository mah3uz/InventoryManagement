     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ProductsFormRequest $request)
    {
        //Store Products Form data to Database
        //$slug = uniqid();
        $product = new Products(array(
            'p_name' => $request->get('p_name'),
            'jan' => $request->get('jan'),
            'price' => $request->get('price'),
        ));

        $product->save();

        return redirect('/products')->with('status', 'Your Product has been saved.');

    }

